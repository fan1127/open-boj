package beans;

/**
 *
 * @author liheyuan
 */
public class UserBean
{
    private int uID;
    private String uUser;
    private String uPass;
    private String uSchool;
    private String uEmail;
    private String uLast;

    /**
     * @return the uID
     */
    public int getUID() {
        return uID;
    }

    /**
     * @param uID the uID to set
     */
    public void setUID(int uID) {
        this.uID = uID;
    }

    /**
     * @return the uUser
     */
    public String getUUser() {
        return uUser;
    }

    /**
     * @param uUser the uUser to set
     */
    public void setUUser(String uUser) {
        this.uUser = uUser;
    }

    /**
     * @return the uPass
     */
    public String getUPass() {
        return uPass;
    }

    /**
     * @param uPass the uPass to set
     */
    public void setUPass(String uPass) {
        this.uPass = uPass;
    }

    /**
     * @return the uSchool
     */
    public String getUSchool() {
        return uSchool;
    }

    /**
     * @param uSchool the uSchool to set
     */
    public void setUSchool(String uSchool) {
        this.uSchool = uSchool;
    }

    /**
     * @return the uEmail
     */
    public String getUEmail() {
        return uEmail;
    }

    /**
     * @param uEmail the uEmail to set
     */
    public void setUEmail(String uEmail) {
        this.uEmail = uEmail;
    }

    /**
     * @return the uLast
     */
    public String getULast() {
        return uLast;
    }

    /**
     * @param uLast the uLast to set
     */
    public void setULast(String uLast) {
        this.uLast = uLast;
    }
}
